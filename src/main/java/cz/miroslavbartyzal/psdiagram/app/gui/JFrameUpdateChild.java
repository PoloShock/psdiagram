    @Override
    public void propertyChange(PropertyChangeEvent evt)
    {
        switch (evt.getPropertyName()) {
            case "status":
                jLabelState.setText((String) evt.getNewValue());
                break;
            case "error":
                jLabelState.setText((String) evt.getNewValue());
                jProgressBar.setIndeterminate(false);
                jProgressBar.setEnabled(false);
                jButton.setText("OK");
                break;
        }
    }

