    public JFrameUpdateChild()
    {
        initComponents();
        jLabelState.setText("");
        super.addMouseListener(new MouseListener()
        {
            @Override
            public void mouseClicked(MouseEvent e)
            {
            }

            @Override
            public void mousePressed(MouseEvent e)
            {
                mouseDownCompCoords = e.getPoint();
            }

            @Override
            public void mouseReleased(MouseEvent e)
            {
                mouseDownCompCoords = null;
            }

            @Override
            public void mouseEntered(MouseEvent e)
            {
            }

            @Override
            public void mouseExited(MouseEvent e)
            {
            }
        });
        super.addMouseMotionListener(new MouseMotionListener()
        {
            @Override
            public void mouseDragged(MouseEvent e)
            {
                Point currCoords = e.getLocationOnScreen();
                setLocation(currCoords.x - mouseDownCompCoords.x,
                        currCoords.y - mouseDownCompCoords.y);
            }

            @Override
            public void mouseMoved(MouseEvent e)
            {
            }
        });

    }

    public void setNoUpdateState()
    {
        if (!jButton.getText().equals("OK")) { // in case of error state, we don't want to lie that we know what is current version
            jProgressBar.setValue(100);
            jProgressBar.setIndeterminate(false);
            jLabelState.setText("PS Diagram je aktuální :)");
            jButton.setText("OK");
        }
    }

